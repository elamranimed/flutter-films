class Film {
  final int id;
  final String title;
  final String director;
  final int year;
  final String description;
  final String genre;
  final double rating;
  final String imageUrl;

  Film({
    required this.id,
    required this.title,
    required this.director,
    required this.year,
    required this.description,
    required this.genre,
    required this.rating,
    required this.imageUrl,
  });
}
